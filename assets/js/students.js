// button click events   



$("#read-more").click(function(){
    if ($(this).text() === 'Read More'){
        $(this).text('Read less');
        $("#quote").css("display", "block").toggle(slow);
    }
    else{
($("#read-more").text() === 'Read Less' )
        $(this).text('Read More');
        $("#quote").css("display", "none").toggle(slow);
    }
});

